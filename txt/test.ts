　　旷野上，当年明月踽踽而行。
　　历史是什么？历史就是那些残台断瓦、古庙荒冢吗？就是那些发黄的书本吗？不是，绝对不是。
　　“历史原来是很精彩的呀！”当年明月大喊一声。声音消失在风中，当年明月继续踽踽地走着，山野一片寂静。
　　好一段时间，远远地，传来一阵阵回声：“很精彩的呀，很精彩的呀，很精彩的呀，很精彩的呀……”声音渐渐消失，山野又归于寂静。
