　　这么多人都围上了当年明月，一下子就热闹起来了。乱哄哄地，有的拍着当年明月的肩膀，有的指手画脚，吵吵嚷嚷，只听清几个词儿：很精彩的，很精彩的，写写吧，写写吧……写吧，写吧，就像写你们公司的老板，那个胖子，写厂子里那个猴儿精小李、前村儿的嘎子、胡同儿里的小三儿，写吧，想到哪儿写哪儿，就这么写吧……
