　　罗伯看起来没注意听他们说什么，他正在往计算机里输入蒂克丝·梅那封邮件的信头里的字符串。“我们在这个数据文件中试一下⋯⋯”全部输入完后他又仔细地检查了一遍拼写，然后按下了回车键。
