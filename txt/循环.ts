　　“没错，最好是第一天！”丹尼看着三个女孩，自己不住地点头，“只是我还想不通我们是怎么做到这一点的。”
　　罗伯指着蒂克丝·梅手中的复印件说：“我可以看看那个电子邮件吗？”
　　他把复印件铺在桌子上，和丹尼一起研究起了里面的内容。
　　拿硬币的埃伦说，“这封邮件里的线索要比普通的侦探小说还多。每次当我们不知所措的时候，就会在这里发现新的暗示。”
