　　罗伯把手肘撑在桌子上。“是啊，我到底过了多少次这令人绝望的第七年啊？”他的声音尖利凄凉，可以想像到他正在体验埃伦曾经描述过的《异次元沙阵》里第一个被上传的小矮人的感觉。
