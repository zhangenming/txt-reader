　　罗伯说，“我有很多的文献，实在是太多了，你能不能告诉我该怎么按比例提高黏合度？”他的手在屏幕上四处指点着，“这些是将近一千升的凝聚物，上百万兆的有效量子比特位。真是不可思议，你们的课题组竟然能使这些量子保持稳定态五十秒。”
